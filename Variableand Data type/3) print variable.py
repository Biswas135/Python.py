# Assigning values to variables
name = "Biswas"      # String variable
age = 23             # Integer variable
price = 25.90        # Float variable

# Printing variables individually
print(name)          # Output: Biswas
print(age)           # Output: 23
print(price)         # Output: 25.9

# Assigning age to another variable
age2 = age           # age2 now has the same value as age

# Printing variables with descriptive text
print("My name is:", name)   # Output: My name is: Biswas
print("My age is:", age)     # Output: My age is: 23

# Printing the copied variable
print(age2)         # Output: 23
