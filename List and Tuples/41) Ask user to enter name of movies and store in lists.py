# Ask user to enter of movies and store in list

movies = []                         # create an empty list

mov1 = input("Enter 1st movie: ")   # take input for 1st movie
mov2 = input("Enter 2nd movie: ")   # take input for 2nd movie
mov3 = input("Enter 3rd movie: ")   # take input for 3rd movie

movies.append(mov1)                 # add 1st movie to the list
movies.append(mov2)                 # add 2nd movie to the list
movies.append(mov3)                 # add 3rd movie to the list

print(movies)                       # print the final list
